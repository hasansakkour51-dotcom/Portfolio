document.addEventListener("DOMContentLoaded", () => {
    // ننتظر تحميل الهيدر من main.js
    const waitForHeader = setInterval(() => {
      const headerBtn = document.getElementById("langToggle");
      const footerBtn = document.getElementById("langToggleFooter");
      const mobileBtn = document.getElementById("langToggleMobile");
  
      // إذا واحد من الأزرار ظهر → الهيدر جاهز
      if (headerBtn || footerBtn || mobileBtn) {
        clearInterval(waitForHeader);
        initTranslation();
      }
    }, 100);
  });
  
  /* ============================
     TRANSLATION SYSTEM
  ============================ */
  
  function initTranslation() {
    let currentLang = localStorage.getItem("lang") || "en";
  
    function setLang(lang) {
      // ترجمة النصوص
      document.querySelectorAll("[data-key]").forEach(el => {
        const key = el.getAttribute("data-key");
        if (translations[lang][key]) {
          el.textContent = translations[lang][key];
        }
      });
  
      // ترجمة الـ placeholders
      document.querySelectorAll("[data-key-placeholder]").forEach(el => {
        const key = el.getAttribute("data-key-placeholder");
        if (translations[lang][key]) {
          el.placeholder = translations[lang][key];
        }
      });
  
      // حفظ اللغة
      localStorage.setItem("lang", lang);
  
      // تحديث نص الأزرار
      const mainBtn = document.getElementById("langToggle");
      const footerBtn = document.getElementById("langToggleFooter");
      const mobileBtn = document.getElementById("langToggleMobile");
  
      if (mainBtn) mainBtn.textContent = lang === "en" ? "AR" : "EN";
      if (footerBtn) footerBtn.textContent = lang === "en" ? "AR" : "EN";
      if (mobileBtn) mobileBtn.textContent = lang === "en" ? "AR" : "EN";
    }
  
    // زر الهيدر
    document.getElementById("langToggle")?.addEventListener("click", () => {
      currentLang = currentLang === "en" ? "ar" : "en";
      setLang(currentLang);
    });
  
    // زر الفوتر
    document.getElementById("langToggleFooter")?.addEventListener("click", () => {
      currentLang = currentLang === "en" ? "ar" : "en";
      setLang(currentLang);
    });
  
    // زر الموبايل
    document.getElementById("langToggleMobile")?.addEventListener("click", () => {
      currentLang = currentLang === "en" ? "ar" : "en";
      setLang(currentLang);
    });
  
    // تشغيل أول مرة
    setLang(currentLang);
  }
  